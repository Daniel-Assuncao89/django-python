numeros = [1, 2, 3, 4, 5]
string = "hello world"

#indice inicial, indice final e sequencia
print(numeros[ : : ])

#mostrar apenas o ultimo caracter
print(numeros[-1::])
print(string[-1::])

#deixar vazio significa q irá até o final do array
print(numeros[0::])
print(string[0::])

#Slicing do começo até a posicao 5
print(numeros[:5])

#Slicing até o final
print(numeros[2:])